# Pico W digital clock + weather display on an ILI9488 480x320 SPI LCD.
#
# Weather and the local UTC offset come from Open-Meteo (free, no key, ~650
# byte responses), with OpenWeatherMap as a backup for temperatures. The old
# BOM scrapers are gone: BOM retired the pages they read in their site
# redesign, and their new JSON API is restricted to registered users.
#
# All errors are non-blocking: the clock keeps drawing, weather slots are
# blanked while their value is unavailable, and problems are shown on a tiny
# status line between the clock and the date.
#
# Wi-Fi credentials and the OpenWeatherMap API key live in settings.toml.
#
# Display driver based on:
# https://www.instructables.com/RPi-Pico-35-Inch-320x480-HVGA-TFT-LCD-ILI9488-Bitm/
# Font bitmaps + width tables are generated by tools/generate_font.py.

